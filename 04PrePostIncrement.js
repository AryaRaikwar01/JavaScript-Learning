let p = 99;
const q = p++;  // Post-increment: assigns 99 to q, then increments p to 100
console.log(`p:${p},q:${q}`);
// console.log("p:" + p + ",q:" + q);
// Output: "p:100,q:99"

let r = 99;
const s = ++r;  // Pre-increment: increments r to 100, then assigns it to s
console.log(`r:${r},s:${s}`);
// console.log("r:" + r + ",s:" + s);
// Output: "r:100,s:100"
// Expected result is r = 100, s = 100


